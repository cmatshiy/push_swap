#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char one;

	if (s)
	{
		one = '\n';
		while (*s != 0)
		{
			write(fd, s, 1);
			s++;
		}
		write(fd, &one, 1);
	}
}
